package com.qa.optionals;

import java.util.Optional;

public class Optionals {
	
	public static void main(String[]args) {

		String x = null;
		
		
	
		Optional<String>myoptional = Optional.ofNullable(x);
		
		System.out.println(myoptional.orElse("oh no"));
		
		String y = "here";
		
		Optional<String>newoptional = Optional.ofNullable(y);
		
		System.out.println(newoptional.orElse("i know where i am"));
		
		
		
	}

}
